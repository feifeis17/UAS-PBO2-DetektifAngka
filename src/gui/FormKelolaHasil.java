    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
       try {
            String sql = "UPDATE pengerjaan_soal SET skor='" + txtSkor.getText() + 
                         "', tanggal_pengerjaan='" + txtTanggal.getText() + 
                         "', jam_pengerjaan='" + txtJam.getText() + 
                         "' WHERE id_pengerjaan='" + txtId.getText() + "'";
                         
            java.sql.Connection conn = config.koneksi.koneksiDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            javax.swing.JOptionPane.showMessageDialog(null, "Data berhasil diubah!");
            tampilData(); // Refresh tabel
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Perubahan gagal: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus data ini?", "Hapus Data", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            try {
                String sql = "DELETE FROM pengerjaan_soal WHERE id_pengerjaan='" + txtId.getText() + "'";
                java.sql.Connection conn = config.koneksi.koneksiDB();
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.execute();
                
                javax.swing.JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
                tampilData(); // Refresh tabel

                txtId.setText("");
                txtSkor.setText("");
                txtTanggal.setText("");
                txtJam.setText("");
                
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(null, "Gagal menghapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed


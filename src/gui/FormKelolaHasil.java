private void tampilData() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Pengguna");
        model.addColumn("Skor");
        model.addColumn("Tanggal");
        model.addColumn("Jam");
        
        jTable1.setModel(model);
        
        try {
            java.sql.Connection conn = config.koneksi.koneksiDB();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery("SELECT id, pengguna, skor, tanggal_pengerjaan, jam_pengerjaan FROM pengerjaan_soal ORDER BY id DESC");
            
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_pengerjaan"), 
                    rs.getString("pengguna"),
                    rs.getString("skor"),
                    rs.getString("tanggal_pengerjaan"),
                    rs.getString("jam_pengerjaan")
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
